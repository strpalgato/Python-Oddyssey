#Ingresar numeros del 1 al 7 y asignarle a dichos numeros los dias de la semana
dia=int (input("Por favor, ingrese un numero del 1 al 7 para determinar a que dia de la semana corresponde el numero"))
if (dia==1):
    print ("El dia 1 equivale a dia Lunes")
elif (dia==2):
    print ("El dia 2 equivale a dia Martes")
elif (dia==3):
    print ("El dia 3 equivale a dia Miercoles")
elif (dia==4):
    print ("El dia 4 equivale a dia Jueves")
elif (dia==5):
    print ("El dia 5 equivale a dia Viernes")
elif (dia==6):
    print ("El dia 6 equivale a dia Sabado")
elif (dia==7):
    print ("El dia 7 equivale a Domingo")
else:
    print ("El numero ingresado no es valido")    